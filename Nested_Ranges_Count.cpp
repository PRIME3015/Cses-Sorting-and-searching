vector<vector<ll>> seg;
void build(ll s, ll e, ll idx, vll &vp)
{
    if (s == e)
    {
        seg[idx].pb(vp[s]);
        return;
    }
    ll mid = (s + e) / 2;
    build(s, mid, 2 * idx + 1, vp);
    build(mid + 1, e, 2 * idx + 2, vp);
   
    merge(all(seg[2 * idx + 1]), all(seg[2 * idx + 2]), back_inserter(seg[idx]));
}

// Query to count elements <= val in range [l, r]
ll query(ll s, ll e, ll l, ll r, ll idx, ll val)
{
    if (s > r || e < l)
        return 0; // Out of range
    if (s >= l && e <= r)
    {
        
        return upper_bound(all(seg[idx]), val) - seg[idx].begin();
    }
    ll mid = (s + e) / 2;
    ll left = query(s, mid, l, r, 2 * idx + 1, val);
    ll right = query(mid + 1, e, l, r, 2 * idx + 2, val);
    return left + right;
}

ll query1(ll s, ll e, ll l, ll r, ll idx, ll val)
{
    if (l > e || r < s)
        return 0;
    if (s >= l && e <= r)
    {

        return seg[idx].end() - lower_bound(all(seg[idx]), val);
    }
    ll mid = (s + e) >> 1;
    ll left = query1(s, mid, l, r, 2 * idx + 1, val);
    ll right = query1(mid + 1, e, l, r, 2 * idx + 2, val);
    return left + right;
}

static bool cmp(vll &v1, vll &v2)
{
    if (v1[0] == v2[0])
        return v1[1] > v2[1];
    return v1[0] < v2[0];
}

